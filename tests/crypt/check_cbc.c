	tcase_add_test(tc, des_decrypt_20);
	tcase_add_test(tc, des_decrypt_32);
	tcase_add_test(tc, des_decrypt_multiple_nullout);
	tcase_add_test(tc, des_decrypt_multiple);

	tcase_add_test(tc, tdea_decrypt_nullout_20);
