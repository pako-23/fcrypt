	tcase_add_test(tc, tdea_encrypt_nullout_32);
	tcase_add_test(tc, tdea_encrypt_20);
	tcase_add_test(tc, tdea_encrypt_32);
	tcase_add_test(tc, tdea_encrypt_multiple_nullout);
