	tcase_add_test(tc, tdea_decrypt_multiple);

	tcase_add_test(tc, invalid_padding);
	tcase_add_test(tc, invalid_padding_ending);
	tcase_add_test(tc, incomplete_block);
	suite_add_tcase(s, tc);

