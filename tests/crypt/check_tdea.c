END_TEST START_TEST(encryption_decryption)
{
	uint8_t key[24];
	uint8_t ciphertext[8];
	uint8_t plaintext[8];
	uint8_t decypted[8];

	for (int i = 0; i < 10; ++i) {
		ck_assert_int_eq(cr_rand_bytes(key, 24), 0);
		ck_assert_int_eq(cr_rand_bytes(plaintext, 8), 0);

		cr_tdea_encrypt(plaintext, key, ciphertext);
		cr_tdea_decrypt(ciphertext, key, decypted);
		ck_assert_mem_eq(plaintext, decypted, 8);
	}
}

